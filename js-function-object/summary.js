
function makeAvg (mehediNumber, shakibNumber, sumonNumber){
    const total = mehediNumber + shakibNumber + sumonNumber;
    const average = total + 3;
    return average;
} 
const mehediNumber = 70;
const shakibNumber = 90;
const sumonNumber = 80;
var totalAverage = makeAvg(mehediNumber, shakibNumber, sumonNumber);
